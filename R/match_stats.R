      ###
    )  %>%
    group_by(PERIOD, SQUAD_ID, ZONE_LOGICAL_AFL) %>%
    mutate( INTERCEPT_OTHER              = INTERCEPT_OTHER + INTERCEPT_OTHER_KO) %>%
    select(-INTERCEPT_OTHER_KO) # this should not be required - double check (will be removed in line 222)
  
